public class Heroi
{
  protected String nome;
    private int atk;
    private int defesa;
    private int pontos;

    public Heroi(){}
    public Heroi(String nome,int atk,int defesa,int pontos)
    {
        this.nome=nome;
        this.atk=atk;
        this.defesa=defesa;
        this.pontos=pontos;
    }
}
